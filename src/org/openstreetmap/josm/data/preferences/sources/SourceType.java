/**
 * The different types of source entries.
 * @since 12649 (moved from gui.preferences package)
 * @since 6670
 */
public enum SourceType {
    /** Entry for a map paint style **/
    MAP_PAINT_STYLE,
    /** Entry for a tagging preset **/
    TAGGING_PRESET,
    /** Entry for a validator tag checker rule **/
    TAGCHECKER_RULE
}
